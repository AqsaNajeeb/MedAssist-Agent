"""# Medical Image Analysis using Groq Vision Model"""

import base64
from pathlib import Path

def encode_image(image_path):
    """Convert image to base64"""
    with open(image_path, "rb") as f:
        return base64.b64encode(f.read()).decode("utf-8")

def analyze_image(image_path, language, user_question=""):
    """Analyze any medical image or lab report photo"""

    # Check file exists
    if not Path(image_path).exists():
        return "❌ Image file not found. Please check the path."

    # Get file extension
    ext = Path(image_path).suffix.lower()
    mime_types = {
        ".jpg": "image/jpeg",
        ".jpeg": "image/jpeg",
        ".png": "image/png",
        ".webp": "image/webp"
    }
    mime_type = mime_types.get(ext, "image/jpeg")

    # Encode image
    image_data = encode_image(image_path)

    # Build prompt based on language
    if language == "URDU":
        if user_question:
            prompt = f"یہ میڈیکل تصویر دیکھیں اور اس سوال کا جواب اردو میں دیں: {user_question}"
        else:
            prompt = """یہ میڈیکل تصویر یا لیب رپورٹ دیکھیں اور اردو میں بتائیں:
1. یہ کس قسم کی تصویر یا رپورٹ ہے؟
2. اہم نتائج کیا ہیں؟
3. کوئی غیر معمولی چیز ہے تو بتائیں
4. مریض کو کیا کرنا چاہیے؟"""
    else:
        if user_question:
            prompt = f"Look at this medical image and answer: {user_question}"
        else:
            prompt = """Analyze this medical image or lab report and explain:
1. What type of image/report is this?
2. What are the key findings?
3. Are there any abnormal values or concerning findings? Flag them with ⚠️
4. What should the patient do next?

Keep the explanation simple and patient-friendly."""

    # Call Groq vision model
    try:
        response = client.chat.completions.create(
            model="meta-llama/llama-4-scout-17b-16e-instruct",  # Groq's vision model
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "image_url",
                            "image_url": {
                                "url": f"data:{mime_type};base64,{image_data}"
                            }
                        },
                        {
                            "type": "text",
                            "text": prompt
                        }
                    ]
                }
            ],
            max_tokens=1024
        )
        return response.choices[0].message.content

    except Exception as e:
        return f"❌ Image analysis failed: {str(e)}"


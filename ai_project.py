"""# Language Detection for English, Urdu, and Roman Urdu Input"""

def detect_language(text):
    # Check for Urdu script characters
    for char in text:
        if '\u0600' <= char <= '\u06FF':
            return "URDU"

    # Check for Roman Urdu common words
    roman_urdu_words = [
        "mjy", "mujhe", "karo", "batao", "hai", "hain", "ky", "ka",
        "ki", "ko", "mn", "mein", "ap", "aap", "kya", "nahi", "aur",
        "krny", "karna", "wala", "hua", "thi", "tha", "hoga"
    ]
    text_lower = text.lower()
    for word in roman_urdu_words:
        if word in text_lower.split():
            return "URDU"

    return "ENGLISH"


"""# PubMed Research Integration for Literature-Based Medical Answers"""

import requests
import xml.etree.ElementTree as ET

def search_pubmed(query, max_results=3):
    """Search PubMed and return paper titles + abstracts"""
    try:
        # Step 1: Search for IDs
        search_url = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/esearch.fcgi"
        search_params = {
            "db": "pubmed",
            "term": query,
            "retmax": max_results,
            "retmode": "json"
        }
        search_resp = requests.get(search_url, params=search_params, timeout=10)
        ids = search_resp.json()["esearchresult"]["idlist"]

        if not ids:
            return "No PubMed articles found for this query."

        # Step 2: Fetch details
        fetch_url = "https://eutils.ncbi.nlm.nih.gov/entrez/eutils/efetch.fcgi"
        fetch_params = {
            "db": "pubmed",
            "id": ",".join(ids),
            "retmode": "xml"
        }
        fetch_resp = requests.get(fetch_url, params=fetch_params, timeout=10)
        root = ET.fromstring(fetch_resp.content)

        papers = []
        for article in root.findall(".//PubmedArticle"):
            title = article.findtext(".//ArticleTitle", "No title")
            abstract = article.findtext(".//AbstractText", "No abstract available")
            year = article.findtext(".//PubDate/Year", "Unknown year")
            papers.append(f"📄 [{year}] {title}\n   {abstract[:300]}...")

        return "\n\n".join(papers)

    except Exception as e:
        return f"PubMed search failed: {str(e)}"


